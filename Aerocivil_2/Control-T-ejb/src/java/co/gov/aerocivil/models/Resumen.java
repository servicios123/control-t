package co.gov.aerocivil.models;

/**
 *
 * @author JUANCAMILOSOSASUAREZ
 */
public class Resumen {
    
    private String alias;
    private Long mananas;
    private Long tardes;
    private Long nochesLS;
    private Long nochesDF;
    private Long amaneceLS;
    private Long amaneceDF;

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }

    public Long getMananas() {
        return mananas;
    }

    public void setMananas(Long mananas) {
        this.mananas = mananas;
    }

    public Long getTardes() {
        return tardes;
    }

    public void setTardes(Long tardes) {
        this.tardes = tardes;
    }

    public Long getNochesLS() {
        return nochesLS;
    }

    public void setNochesLS(Long nochesLS) {
        this.nochesLS = nochesLS;
    }

    public Long getNochesDF() {
        return nochesDF;
    }

    public void setNochesDF(Long nochesDF) {
        this.nochesDF = nochesDF;
    }

    public Long getAmaneceLS() {
        return amaneceLS;
    }

    public void setAmaneceLS(Long amaneceLS) {
        this.amaneceLS = amaneceLS;
    }

    public Long getAmaneceDF() {
        return amaneceDF;
    }

    public void setAmaneceDF(Long amaneceDF) {
        this.amaneceDF = amaneceDF;
    }
    
    
}
